package javaNote;

public class Variable {
	public static void main(String[] args) {
		char c1 = 65;
		char c2 = 'A';
		
		System.out.println("c1 : " + c1);
		System.out.println("c2 : " + c2);
		
		int i1 = c1;
		int i2 = c2;
		
		System.out.println("i1 : " + i1);
		System.out.println("i2 : " + i2);
	}
}
